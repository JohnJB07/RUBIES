    private void calculateAndPopulateTable(String paymentMethod) {
        boolean isCash = paymentMethod.equals("Cash");
        
        jLabel6.setVisible(!isCash);
        jScrollPane2.setVisible(!isCash);
        jLabel5.setVisible(!isCash);
        jComboBox2.setVisible(!isCash);
        jTextField3.setVisible(!isCash);
        
        this.pack();

        if (isCash) {
            return;
        }

        double propertyPrice = lot.getTotalValue();
        double downPayment = lot.getDownpaymentFee();
        double loanAmount = propertyPrice - downPayment;

        DecimalFormat df = new DecimalFormat("#,##0.00");

        double annualRate = 0.09;
        int maxYears = 30;

        if (paymentMethod.equals("Bank")) {
            annualRate = 0.07; 
            maxYears = 20; 
        }

        double monthlyRate = annualRate / 12.0;
        int[] yearsArray = {30, 25, 20, 15, 10, 5};

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); 

        jComboBox2.removeAllItems();

        for (int i = 0; i < yearsArray.length; i++) {
            int years = yearsArray[i];

            if (years > maxYears) {
                continue;
            }
            int totalMonths = years * 12;
            double monthlyPayment = 0;
            double gmiRequired = 0;

            if (loanAmount > 0) {
                double mathPower = Math.pow(1 + monthlyRate, totalMonths);
                monthlyPayment = loanAmount * (monthlyRate * mathPower) / (mathPower - 1);
                gmiRequired = monthlyPayment / 0.30;
            }

            model.addRow(new Object[]{years + " years", "Php " + df.format(monthlyPayment), "Php " + df.format(gmiRequired)});
            
            jComboBox2.addItem(years + " years");
        }
    }

