    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String paymentMethod = jComboBox1.getSelectedItem().toString();
        boolean isCash = paymentMethod.equals("Cash");

        double userBalance = user.getCustomer()[User.getLoginIndx()].getBalance();
        double requiredPayment;
        
        //if Cash payment method
        if (isCash) {
            requiredPayment = lot.getTotalValue();
        } else {
            requiredPayment = lot.getDownpaymentFee();
        }        

        if (userBalance < requiredPayment) {
            JOptionPane.showMessageDialog(this, "Insufficient balance.");
            return; 
        }
        //If Pag-ibig or Bank payment method
        double loanAmount = 0;
        
        if (!isCash) {
            if (jComboBox2.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Please select a loan term.");
                return;
            }
            
            String selectedTerm = jComboBox2.getSelectedItem().toString();
            double userGMI = user.getCustomer()[User.getLoginIndx()].getGMI();
            double requiredGMI = 0;

            for (int i = 0; i < jTable2.getRowCount(); i++) {
                String tableTerm = jTable2.getValueAt(i, 0).toString();
                
                if (tableTerm.equals(selectedTerm)) {
                    String gmiString = jTable2.getValueAt(i, 2).toString().replaceAll("[^\\d.]", ""); //replaceAll removes Php from Monthly payment
                    requiredGMI = Double.parseDouble(gmiString);
                    break;
                }
            }

            if (userGMI < requiredGMI) {
                JOptionPane.showMessageDialog(this, "Gross Monthly Income too low for this term.");
                return; 
            }
            
            loanAmount = lot.getTotalValue() - lot.getDownpaymentFee();
        }

        System.out.println("[UPDATE]: Confirming Dialog");
        if (JOptionPane.showConfirmDialog(jButton1, "Would you like to buy the lot?", 
                "Notice", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            if (lot.getStatus() == "Reserved") {
                if (Lot.getUserLoginIndex() == User.getLoginIndx()) {
                    int result = dashboard.buyTheProperty(lot);
                    if (!lot.getStatus().equals("Sold")) {
                        try {
                            lot.setStatus("Sold");
                            System.out.println("[UPDATE]: Bought lot");

                            DecimalFormat df = new DecimalFormat("#,##0.00");
                            if (!isCash) {
                                double currentOwed = user.getCustomer()[User.getLoginIndx()].getTotalOwed(); 
                                user.getCustomer()[User.getLoginIndx()].addTotalOwed(currentOwed + loanAmount); 
                            }

                            JOptionPane.showMessageDialog(jButton1, "Bought the lot.");
                            dispose();
                        } catch(Exception e) {
                            System.out.println("[ERROR]: Unexpected behaviour occured.");
                        }
                    }
                }
            } else if (lot.getStatus().equals("Sold")) {
                System.out.println("[NOTICE]: Lot cannot be reserved or bought");
                JOptionPane.showMessageDialog(jButton1, "Lot is not available.");
            } else {
                int result = dashboard.buyTheProperty(lot);
                if (!lot.getStatus().equals("Sold")) {
                    try {
                        lot.setStatus("Sold");
                        System.out.println("[UPDATE]: Bought lot");
                            
                        DecimalFormat df = new DecimalFormat("#,##0.00");
                        if (!isCash) {
                            double currentOwed = user.getCustomer()[User.getLoginIndx()].getTotalOwed(); 
                            user.getCustomer()[User.getLoginIndx()].addTotalOwed(currentOwed + loanAmount); 
                        }
                        
                        JOptionPane.showMessageDialog(jButton1, "Bought the lot.");
                        dispose();
                    } catch(Exception e) {
                        System.out.println("[ERROR]: Unexpected behaviour occured.");
                    }
                }
            }
        } else {
            System.out.println("[UPDATE]: Did not reserve lot");
        }
        
        dashboard.updateBalanceLabel();
        dashboard.updateTotalOwed();
    }//GEN-LAST:event_jButton1ActionPerformed

