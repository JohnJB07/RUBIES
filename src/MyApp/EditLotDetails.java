    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            Lot selected = re.getLot()[selectedRow];
            try {
                selected.setTotalValue(Double.parseDouble(jTable1.getValueAt(selectedRow, 2).toString()));
                selected.setReservationFee(Double.parseDouble(jTable1.getValueAt(selectedRow, 3).toString()));
                selected.setDownpaymentFee(Double.parseDouble(jTable1.getValueAt(selectedRow, 4).toString()));
                selected.setStatus(jTable1.getValueAt(selectedRow, 5).toString());
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Invalid number format");
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

