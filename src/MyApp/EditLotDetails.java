    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Lot selected = lotList.getSelectedValue();
            if (selected != null) {
                try {
                    selected.setTotalValue(Double.parseDouble(jTextField1.getText()));
                    selected.setReservationFee(Double.parseDouble(jTextField2.getText()));
                    selected.setDownpaymentFee(Double.parseDouble(jTextField3.getText()));
                    selected.setStatus((String) jComboBox1.getSelectedItem());
                } catch (NumberFormatException ex) {
                    JOptionPane.showMessageDialog(this, "Invalid number format");
                }
            }
    }//GEN-LAST:event_jButton1ActionPerformed

