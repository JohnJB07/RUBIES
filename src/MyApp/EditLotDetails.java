        tableModel = new javax.swing.table.DefaultTableModel(columnNames, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return true; // allow editing directly in table cells
            }
        };

        for (Lot l : re.getLot()) {
            if (l != null) {
                Object[] row = {
                    l.getHouse().getBlockNumber(),
                    l.getLotNumber(),
                    String.format("%.2f", l.getTotalValue()),
                    String.format("%.2f", l.getReservationFee()),
                    String.format("%.2f", l.getDownpaymentFee()),
                    l.getStatus()
                };
                tableModel.addRow(row);
            }
        }

        jTable1.setModel(tableModel);
        jTable1.setRowHeight(30);
        jTable1.setFont(new java.awt.Font("Segoe UI", java.awt.Font.PLAIN, 16));
        
