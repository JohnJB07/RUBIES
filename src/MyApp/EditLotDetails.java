    public EditLotDetails(Admin admin, User user, RealEstate re) {
        this.realEstate = re;
        this.user = user;
        this.admin = admin;
        DefaultListModel<Lot> model = new DefaultListModel<>();
        for (Lot l : realEstate.getLot()) {
        if (l != null) { // avoid nulls
            model.addElement(l);
        }
    }
    lotList = new JList<>(model);

        initComponents();
        jScrollPane1.setViewportView(lotList);
        
        lotList.addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            Lot selected = lotList.getSelectedValue();
            if (selected != null) {
                jTextField1.setText(String.valueOf(selected.getTotalValue()));
                jTextField2.setText(String.valueOf(selected.getReservationFee()));
                jTextField3.setText(String.valueOf(selected.getDownpaymentFee()));
                jComboBox1.setSelectedItem(selected.getStatus());
        }
    }
});

    }

