    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Startup startupWindow = new Startup();
        startupWindow.setResizable(false);
        
        String username = jTextField1.getText().toString(); // Username
        String password = jPasswordField1.getText().toString(); //password
 
        int result = user.login(username, password);
        
        switch(result) {
            case -1:
                jLabel4.setText("Account does not exist");
                break;
            case 0: // Go to dashboard
                if (User.getLoginType() == 1) {
                    AgentAvailabilityWindow availabilityWindow = new AgentAvailabilityWindow(admin, user, re);
                    availabilityWindow.setLocationRelativeTo(null);
                    availabilityWindow.setVisible(true);
                    dispose();
                } else if (User.getLoginType() == 2) {
                    Dashboard dashboard = new Dashboard(user, re);
                    dashboard.setLocationRelativeTo(null);
                    dashboard.setVisible(true);
                    dispose();
                }
                break;
            case 1: // Admin login
                Directory directoryWindow = new Directory(admin, user, re);
                directoryWindow.setLocationRelativeTo(null);
                directoryWindow.setVisible(true);
                dispose();
                break;
            case 2:
                jLabel4.setText("Wrong password");
                break;
            case 3:
                jLabel4.setText("Incomplete input field");
                break;
            case 4:
                jLabel4.setText("Agent is disabled.");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

